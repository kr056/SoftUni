package p22_circleIntersection;

class Circle {
    private Point Center;
    private int Radius;


    Point getCenter() {
        return Center;
    }

    void setCenter(Point center) {
        Center = center;
    }

    int getRadius() {
        return Radius;
    }

    void setRadius(int radius) {
        Radius = radius;
    }
}
